package com.atlas.model;

public class Wheels {
	private Spokes spokes;
	private Rim rim ;
	private Tube tube;
	private Tyre tyre;
	public Spokes getSpokes() {
		return spokes;
	}
	public void setSpokes(Spokes spokes) {
		this.spokes = spokes;
	}
	public Rim getRim() {
		return rim;
	}
	public void setRim(Rim rim) {
		this.rim = rim;
	}
	public Tube getTube() {
		return tube;
	}
	public void setTube(Tube tube) {
		this.tube = tube;
	}
	public Tyre getTyre() {
		return tyre;
	}
	public void setTyre(Tyre tyre) {
		this.tyre = tyre;
	}

}
